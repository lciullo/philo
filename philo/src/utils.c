#include "philo.h"

size_t	ft_strlen(const char *str)
{
	size_t	i;

	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	return (i);
}

int	ft_isdigit(int c)
{
	if (c >= '0' && c <= '9')
		return (SUCCESS);
	return (FAILURE);
}

int	ft_strcmp(char *s1, char *s2)
{
	int	i;

	i = 0;
	if (!s1 || !s2)
		return (FAILURE);
	while (s1[i] != '\0' && s2[i] != '\0' && s1[i] == s2[i])
		i++;
	return (s1[i] - s2[i]);
}

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*str;

	i = 0;
	str = (unsigned char *) s;
	while (i < n)
	{
		str[i] = '\0';
		i++;
	}
}

void	*ft_calloc(size_t nb_elements, size_t size)
{
	void	*ptr;

	if (!nb_elements || !size)
		return (malloc(0));
	if (size != 0 && nb_elements > SIZE_MAX / size)
		return (NULL);
	ptr = malloc(nb_elements * size);
	if (!ptr)
		return (NULL);
	ft_bzero(ptr, nb_elements * size);
	return (ptr);
}
